#slicing and indexing
#below is indexing
mystring = ('Hello World')
print (mystring)
print (mystring [-3])
print (mystring [8])
#below is slicing
mystring = ('dogcatratmatpatsat')
print ('\n')
print (mystring)
print ('\n')
print (mystring [3:])
print (mystring [:15])
print (mystring [6:9])
#changing the step sizes
print ('\n')
print (mystring [::3])
print (mystring[::-1])
